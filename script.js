const calendar = document.getElementById("calendar");
const yearSelect = document.getElementById("yearSelect");
const monthSelect = document.getElementById("monthSelect");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");

let currentYear = 2026;
let currentMonth = 05;

// 年プルダウン作成
for (let year = 2025; year <= 2026; year++) {

  const option = document.createElement("option");

  option.value = year;

  option.textContent = `${year}年`;

  yearSelect.appendChild(option);

}

// カレンダー描画
function renderCalendar(year, month) {

  calendar.innerHTML = "";

  yearSelect.value = year;
  monthSelect.value = month;

  const firstDay = new Date(year, month - 1, 1);

  const lastDay = new Date(year, month, 0);

  const startWeekDay = firstDay.getDay();

  const daysInMonth = lastDay.getDate();

  // 空白マス
  for (let i = 0; i < startWeekDay; i++) {

    const emptyBox = document.createElement("div");

    emptyBox.classList.add("day", "empty");

    calendar.appendChild(emptyBox);

  }

  // 日付生成
  for (let day = 1; day <= daysInMonth; day++) {

    const dayBox = document.createElement("div");

    dayBox.classList.add("day");

    const weekDay = new Date(year, month - 1, day).getDay();

    // 日曜
    if (weekDay === 0) {

      dayBox.classList.add("sunday");

    }

    // 土曜
    if (weekDay === 6) {

      dayBox.classList.add("saturday");

    }

    // yyyy-mm-dd
    const dateKey =
      `${year}-${String(month).padStart(2, "0")}-${String(day).padStart(2, "0")}`;

    // ツイートある日
    if (tweets[dateKey]) {

      dayBox.classList.add("hasTweet");

    }

    // 日付表示
    dayBox.innerHTML = `<div class="date">${day}</div>`;

    // ツイート表示
    if (tweets[dateKey]) {

      tweets[dateKey].forEach(tweet => {

        const link = document.createElement("a");

        link.href = tweet.url;

        link.textContent = tweet.title;

        link.target = "_blank";

        dayBox.appendChild(link);

      });

    }

    calendar.appendChild(dayBox);

  }

}

// 前の月
prevBtn.addEventListener("click", () => {

  currentMonth--;

  if (currentMonth === 0) {

    currentMonth = 12;
    currentYear--;

  }

  if (currentYear < 2025) {

    currentYear = 2025;
    currentMonth = 1;

  }

  renderCalendar(currentYear, currentMonth);

});
// 次の月
nextBtn.addEventListener("click", () => {

  currentMonth++;

  if (currentMonth === 13) {

    currentMonth = 1;
    currentYear++;

  }

  if (currentYear > 2026) {

    currentYear = 2026;
    currentMonth = 12;

  }

  renderCalendar(currentYear, currentMonth);

});
// 年変更
yearSelect.addEventListener("change", () => {

  currentYear = Number(yearSelect.value);

  renderCalendar(currentYear, currentMonth);

});

// 月変更
monthSelect.addEventListener("change", () => {

  currentMonth = Number(monthSelect.value);

  renderCalendar(currentYear, currentMonth);

});

// 初回表示
renderCalendar(currentYear, currentMonth);